            <h4>Jenis Foto</h4>
            <ul>
                <li>Apaya tidak menerima pesanan cetak foto yang mengandung unsur pornografi / dinilai terlalu seronok / pelanggaran SARA.</li>
                <li>Pesanan yang tidak sesuai dengan syarat jenis foto akan Apaya konfirmasikan kepada pembeli.</li>
                <li>Apaya tidak bertanggungjawab terhadap keaslian, kepemilikan, dan hak cipta foto. Apabila terjadi masalah terkait hal tersebut, maka tanggung jawab sepenuhnya ada pada pembeli.</li>
                <li>Apaya menjaga kerahasiaan file foto yang diupload oleh pembeli, namun tidak bertanggung jawab terhadap foto yang berada di jejaring sosial yang terhubung dengan akun pembeli.</li>
