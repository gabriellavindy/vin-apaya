                <li>Apaya memeriksa laporan berdasarkan nomor pesanan, lalu mengkonfirmasi ke pembeli.</li>
                <li>Apaya akan mengirimkan produk pengganti dengan ketentuan pemrosesan seperti pada poin Proses Cetak.</li>
                <li>Jika pembeli memilih untuk refund (pengembalian dana), setelah mendapatkan konfirmasi dari Apaya, pembeli harap mengirimkan nomor rekening bersama nama pemilik rekening ke Kontak Apaya. Apaya hanya akan melakukan pengembalian ke alamat rekening yang sama dengan pembelian.</li>
                <li>Apaya akan mengirimkan kembali dana yang telah ditransfer pembeli sejumlah harga total pesanan, di luar ongkos kirim.</li>
