            <h4>Proses Cetak</h4>
            <ul>
                <li>Apaya akan memulai mencetak pesanan setelah mendapatkan konfirmasi transfer dari pembeli dan melakukan verifikasi nilai transfer melalui media komunikasi yang disediakan.</li>
                <li>Apaya tidak meng-edit foto pelanggan sama sekali, selain pemotongan dan pengubahan ukuran untuk kebutuhan otomasi cetak resolusi tinggi. Seluruh kontur warna, tingkat keterangan, dan resolusi asli foto bergantung dari foto pelanggan sendiri. Apaya tidak bertanggungjawab terhadap rendahnya kualitas foto yang dipilih oleh pelanggan. Apaya hanya menjamin kualitas cetak yang bagus dengan mesin terbaik kami.</li>
                <li>Pencetakan foto Apaya membutuhkan waktu maksimal 3 hari kerja (*) sebelum barang dapat dikirimkan ke pembeli. Namun normalnya, apabila pembeli melakukan konfirmasi transfer sebelum jam 1 siang di hari pemesanan, order akan dicetak hari itu juga dan dikirm paling lambat esok harinya.</li>
                <li>Apabila terjadi keterlambatan pemrosesan sampai melebihi 3 hari, Apaya akan mengabarkan pada pembeli.</li>
                <li>Status pemrosesan order Anda dapat dilihat pada halaman Riwayat Belanja setelah Anda melakukan login.</li>
            </ul>
            <p>(*) Untuk beberapa produk yang pengerjaannya kompleks, seperti Concept Photobook yang membutuhkan waktu penjilidan lebih lama, maka prosesnya akan lebih lama pula demi menjamin kualitas hasil pengerjaan.</p>
