            <h4>Pengiriman</h4>
            <ul>
                <li>Pengiriman order menggunakan jasa Pos Indonesia, dengan estimasi paket diterima 3 - 5 hari kerja.</li>
                <li>Nomor resi pengiriman dapat Anda lihat pada halaman History Order setelah Anda melakukan login.</li>
                <li>Apaya melakukan pengepakan dengan aman, dan hanya bertanggung jawab pada pesanan sampai dengan pengiriman pada kantor Pos Indonesia.</li>
                <li>Kerusakan ataupun kehilangan yang terjadi dalam proses pengiriman oleh kurir, berada di luar tanggung jawab Apaya.</li>
            </ul>
